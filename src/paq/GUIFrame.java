    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();
        Buffer buffer = new Buffer(Integer.parseInt(jTextField3.getText()), model, model2);
        int value = (Integer) jSpinner1.getValue();
        int valueC = (Integer) jSpinner2.getValue();
        
        int esperaP = Integer.parseInt(jTextField1.getText());
        int esperaC = Integer.parseInt(jTextField2.getText());
        
        int rangoUno = Integer.parseInt(jTextField4.getText());
        int rangoDos = (Integer)jSpinner3.getValue();
        
        
        List<Producer> productores = new ArrayList<Producer>();
        for(int i=0;i < value; i++){
            productores.add(new Producer(buffer,i,rangoUno,rangoDos, esperaP));
        }
        
        List<Consumer> consumidores = new ArrayList<Consumer>();
        for(int i=0;i < valueC; i++){
            consumidores.add(new Consumer(buffer,i, esperaC, model2));
        }
        
        for (Producer p : productores) {
            p.start();
        }
        
        for (Consumer c : consumidores) {
            c.start();
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

